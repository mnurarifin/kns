            this.getCount()
        },
    })

    const product = Vue.createApp({
        data() {
            return {
                list_product: []
            }
        },
        methods: {
            getListProduct() {
                $.ajax({
                    url: "<?= BASEURL ?>/home/get-product",
                    type: "GET",
                    data: {},
                    success: (res) => {
                        let data = res.data.results
                        let html = ``
                        let owl_product = $('#product');

                        $.each(data, (key, val) => {
                            html += `<div class="product-card style3">
                                    <div class="product-img">
                                        <img src="${val.product_image}" alt="Image">
                                    </div>
                                    <div class="product-info">
                                        <h3><a href="#">${val.product_name}</a></h3>
                                        <p class="price">${this.formatCurrency(val.product_price)}</p>
                                    </div>
                                </div>`
                        })
                        owl_product.html(html)
                        owl_product.trigger('destroy.owl.carousel');
                        owl_product.owlCarousel({
                            loop: true,
                            // nav: true,
                            responsiveClass: true,
                            margin: 10,
                            dots: false,
                            responsive: {
                                0: {
                                    items: 1
                                },
                                600: {
                                    items: 2
                                },
                                1000: {
                                    items: 4
                                }
                            }
                        });
                    }
                })
            },
            formatCurrency(params) {
                let formatter = new Intl.NumberFormat('id-ID', {
                    style: 'currency',
                    currency: 'IDR',
                    minimumFractionDigits: 0,
                    maximumFractionDigits: 0,
                })
                return formatter.format(params)
            }
        },
        mounted() {},
    }).mount('#vue-app-product')

