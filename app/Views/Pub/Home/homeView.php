    const banner = Vue.createApp({
        data() {
            return {
                list_banner: []
            }
        },
        methods: {
            getContent() {
                $.ajax({
                    url: "<?= BASEURL ?>/home/get-banner",
                    type: "GET",
                    data: {},
                    success: (res) => {
                        let data = res.data.results
                        let html = ``
                        let owl_banner = $('#banner');

                        $.each(data, (key, val) => {
                            html += `<div class="hero-slide-item" data-dot="${val.banner_dot}">
                                <div class="row align-items-center">
                                    <div class="col-md-12">
                                        <div class="hero-img-wrap">
                                            <img src="${val.banner_image}" alt="Image" class="lg-slide">
                                            <img src="${val.banner_image_mobile}" class="mobile-slide" alt="Image">
                                        </div>
                                        <div class="hero-content">
                                            <h1>${val.banner_title}</h1>
                                            <p>${val.banner_description}</p>
                                            <!-- <a href="shop-right-sidebar.html" class="btn style2">Shop Now</a> -->
                                        </div>
                                    </div>
                                </div>
                            </div>`
                        })

                        owl_banner.html(html)

                        owl_banner.trigger('destroy.owl.carousel');

                        owl_banner.owlCarousel({
                            items: 1,
                        })
                    }
                })
            }
        },
        mounted() {},
    }).mount('#vue-app-banner')

    const count = Vue.createApp({
        data() {
            return {
                data_count: {}
            }
        },
        methods: {
            getCount() {
                $.ajax({
                    url: "<?= BASEURL ?>/home/get-count",
                    type: "GET",
