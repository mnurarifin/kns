                                    </ul>
                                    <h3><a href="#">${val.content_title}</a></h3>
                                </div>
                            </div>`
                        })
                        owl_content.html(html)
                        owl_content.trigger('destroy.owl.carousel');
                        owl_content.owlCarousel({
                            items: data.length <= 4 ? data.length : 4,
                        });
                    }
                })
            }
        },
        mounted() {},
    })
